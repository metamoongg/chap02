public class CharExample {

	public static void main(String[] args) {
		char c1 = 0;
		char c2 = 0;
		char c3 = 0;
		
		c1 = 'A';
		c2 = 65;
		c3 = 0b0100_0001;
	//  char 타입은 숫자를 입력하면 그 숫자에 맞는 문자열을 출력한다(아스키코드 참고)	
		
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(c3);
		
	}

}
